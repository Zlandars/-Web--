function fixOffset(localTS, o, tz) {
  
  let utcGuess = localTS - o * 60 * 1000; 
  const o2 = tz.offset(utcGuess);
  if (o === o2) {
    return [utcGuess, o];
  } 
  utcGuess -= (o2 - o) * 60 * 1000; 
  const o3 = tz.offset(utcGuess);
  if (o2 === o3) {
    return [utcGuess, o2];
  } 
  return [localTS - Math.min(o2, o3) * 60 * 1000, Math.max(o2, o3)];
} 
